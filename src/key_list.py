#Key list class, has a naem.

class key_list:

    def __init__(self, keychars, name):
        self.keychars = keychars
        self.name = name

    def get_name(self):
        return self.name

    def get_keychars(self):
        return self.get_keychars

    def update_keychars(self, keychars):
        self.keychars = keychars